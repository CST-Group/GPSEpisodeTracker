package regions;

public class Circle {
    private double x;
    private double y;
    private double radius;

    public Circle(double x, double y, double r) {
        this.x = x;
        this.y = y;
        this.radius = r;
    }

    public double getX() {
        return x;
    }

    public double getY() {
        return y;
    }

    public double getRadius() {
        return radius;
    }
}
